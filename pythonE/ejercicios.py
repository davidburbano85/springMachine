numerouno= int(input('ingrese el primer número por favor: '))
numerodos= int(input('ingrese el segundo número por favor: '))


#operaciones aritmeticas:
suma=numerouno+numerodos
print('la suma es entre ',  numerouno,'y', numerodos, 'es:', suma)
multiplicacion=numerouno*numerodos
print('la multiplicacion es entre ',  numerouno,'y', numerodos, 'es:', multiplicacion)


#operaciones de comparacion
verificar_igualdad=numerouno==numerodos
print('el numero:', numerouno, 'es igual que:', numerodos,'?', verificar_igualdad)

mayor_que=numerodos>numerouno
print('el numero:', numerouno, 'es menor que:', numerodos, '?', mayor_que)


